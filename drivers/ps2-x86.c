static void sendByte(int sendPort, int cmd) {
    // wait while input buffer is full
    while ((inb(0x64) & 0x2) == 1) {};
    // send command
    outb(sendPort, cmd);
}

static int readByte() {
    // wait while output buffer is empty
    while ((inb(0x64) & 0x1) == 0) {};
    return inb(0x60);
}

static void flushBytes() {
   while((inb(0x64) & 0x1) == 1){
       readByte();
   }
}

static void ASSERT(int a) {};

#define CNTR_P 0x64
#define DATA_P 0x60

void PS2_init() {
    // Write to the PS2 Controller Status Register
    // http://wiki.osdev.org/%228042%22_PS/2_Controller#Status_Register
    sendByte(CNTR_P, 0x60);
    sendByte(DATA_P, 0x32);
    flushBytes();

    // Restart Keyboard + Self Test
    sendByte(DATA_P, 0xFF);
    ASSERT(readByte() == 0xAA);

    // Send scan set command
    sendByte(DATA_P, 0xF0);
    flushBytes();
    // Set scan set to 1 
    sendByte(DATA_P, 0x01);
    flushBytes();
    
    // Send scan set command
    sendByte(DATA_P, 0xF0);
    flushBytes();
    // Request scan set
    sendByte(DATA_P, 0x00);
    ASSERT(readByte() == 0x01);
}

int PS2_readChar() {
    return scan_to_ascii[readByte()].no_shift;
}
